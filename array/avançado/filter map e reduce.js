//filter
//retornar valores maiores que 10
const numeros = [5, 10, 50, 70, 80, 2, 76, 42]

const valores = numeros.filter(valor =>  valor > 10)
console.log(valores)


//map 
//Retorna o dobro dos valores
const numeross = [5, 10, 50, 70, 80, 2, 76, 42]

const dobro = numeross.map(valor => valor * 2)
console.log(dobro)


